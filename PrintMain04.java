package kr.a01.basic;

public class PrintMain04 {
	public static void main(String[] args) {
		//숫자(정수)
		System.out.println(23);
		System.out.println("23");//문자열
		
		//숫자(실수)
		System.out.println(25.67);
		System.out.println("25.67"); //문자열
		
		//논리값(boolean)
		System.out.println(true);
		System.out.println(false);
		System.out.println("true"); //문자열
		System.out.println("false"); //문자열
		
		//문자 : 한 문자
		System.out.println('A');
		System.out.println('가');
		//문자열을 ''로 감쌌기 때문에 오류
		//System.out.println('바다');
		
		//문자열 : 한 문자 이상
		System.out.println("한강");
		System.out.println("Bus");
		System.out.println("25");
		System.out.println("B");
	}
}











