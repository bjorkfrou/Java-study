package kr.a12.objectbasic.extention;

//전화의 기본적인 기능을 가지고 있는 클래스
public class Phone {
	protected String number;
	protected String model;
	protected String color;
	
	public String getNumber() {
		return number;
	}
	public String getModel() {
		return model;
	}
	public String getColor() {
		return color;
	}
}





